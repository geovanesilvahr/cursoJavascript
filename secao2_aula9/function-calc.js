function somar(num1, num2){
    console.log(num1 + num2);
}

function mult(n1, n2){
    console.log(n1 * n2);
}

function dividir(number1, number2){
    console.log(number1 / number2);
}

function subtrair(nu1, nu2){
    console.log(nu1 - nu2);
}

//somar
console.log(somar(20, 25))

//multi
console.log(mult(20, 25))

//dividir
console.log(dividir(20, 25))

//subtrair
console.log(subtrair(20, 25))